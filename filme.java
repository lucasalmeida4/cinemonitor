class Filme {

String titulo;
int ano;
int tipo;

public void setTitulo(String t) {
  titulo = t;
}

public void setAno(int a) {
  ano = a;
}
public void setTipo(int ti) {
  tipo = ti;
}
public String getTitulo() {
  return titulo;
}
public int getAno() {
  return ano;
}
public int getTipo() {
  return tipo;
}







}